def doxygen_autoconf_doc_diff(project_config, commit_id):
    """Compare HTML files from documentations of a commit generated
    by Doxygen and ConfiGen, and generate HTML diffs using DaisyDiff.

    The directory structure is kept same as the documentation of a commit.
    To see the diff, open the index.html file in a browser same as the way
    you open the index.html file for a commit's documentation.
    Use the left side navigation panel and select a file. If there are changes
    between the two commits' documentation for this file, you will see those 
    changes in green or red colors. Otherwise, the file's documentation will
    look similar to a normal documentation file.

    Note: It is assumed that the DaisyDiff repository is cloned and kept
    in the same directory level as this project's repository - kconfig_doc.
    For example, if 'kconfig_doc' is cloned and kept under 'configen'
    directory, then 'DaisyDiff' should also be cloned and kept under
    'configen' directory. Otherwise, this function cannot not access the jar
    file properly.

    Parameters
    ----------
    project_config
        Project specific config options. i.e., variables from 
        config_bbox.py file for BusyBox.
    commit_id : str
        Commit id for which the documentation will be compared with.
    """
    logging.info('[{}] Generating Doxygen and ConfiGen diff for commit {}'
                 .format(project_config.project_name, commit_id[:7]))

    daisydiff_dir = os.path.join(os.path.dirname(root_dir), 'DaisyDiff')
    daisydiff_jar = os.path.join(daisydiff_dir, 'target',
                                 'daisydiff-1.2-jar-with-dependencies.jar')
    if daisydiff_jar == None:
        logging.error(('[{}][doxygen_autoconf_doc_diff] Could not find '
                       'DaisyDiff jar file. '
                      'Follow README to build the jar file.')
                      .format(project_config.project_name))
        return -1

    doxygen_doc_dir = os.path.join(project_config.project_doxygen_doc_dir,
                                   commit_id, 'html')
    autoconf_doc_dir = os.path.join(project_config.project_doc_dir,
                                    commit_id, 'html')

    # Check documentation exist or not.
    # Documentation 'html' dir might exist with only 'kconfig' dir containing
    # XML files. That means, doxygen only generated XML files and HTML files
    # are not generated. Check this by counting number of items in 'html' dir.
    # If does not exist the documentation, show error and exit.
    if not os.path.isdir(doxygen_doc_dir) or len(os.listdir(doxygen_doc_dir)) < 2:
        logging.error('[{}][doxygen_autoconf_doc_diff] Doxygen documentation does not exist for commit {}.'
                      .format(project_config.project_name, commit_id))
        return -1
    if not os.path.isdir(autoconf_doc_dir) or len(os.listdir(autoconf_doc_dir)) < 2:
        logging.error('[{}][doxygen_autoconf_doc_diff] ConfiGen documentation does not exist for commit {}.'
                      .format(project_config.project_name, commit_id))
        return -1

    diff_dir = os.path.join(project_config.project_doxy_auto_diff_dir,
                            commit_id[:7] + '_' + commit_id[:7], 'html')
    create_dir(diff_dir)
    copy_doxygen_files(autoconf_doc_dir, diff_dir)

    filenames = extract_filenames(project_config, commit_id)
    # filenames = {'loginutils/chpasswd.c': {'left': '/home/jobayer/workspace/kconfig_doc/commit_parser/data/temp/doxygen_doc/busybox/70f77e4617e06077231b8b63c3fb3406d7f8865d/html/dc/d8a/chpasswd_8c.html', 'right': '/home/jobayer/workspace/kconfig_doc/commit_parser/data/temp/autoconf_doc/busybox/70f77e4617e06077231b8b63c3fb3406d7f8865d/html/dc/d8a/chpasswd_8c.html'}}

    bar = progressbar.ProgressBar(max_value=len(filenames),
                                  redirect_stdout=True).start()
    added_files, deleted_files, changed_files = {}, {}, {}
    index = 0
    for filename, pair in filenames.items():
        if 'left' in pair:
            if 'right' in pair: # modified
                copy_daisydiff_files(daisydiff_dir, diff_dir,
                                     pair['right'])
                diff_file = generate_html_diff(project_config, pair['left'],
                                               pair['right'], diff_dir,
                                               filename, daisydiff_jar)
                change_obj = diff_changes(diff_file)
                if change_obj:
                    changed_files[filename] = {k: v for k, v in change_obj.items()}
            else: # deleted
                empty_file = os.path.join(project_config.project_doc_dir,
                                          commit_id, 'html', 'del',
                                          'a' + str(len(deleted_files)),
                                          os.path.basename(pair['left']))
                create_empty_html_file(empty_file)
                copy_daisydiff_files(daisydiff_dir, diff_dir, empty_file)
                diff_file = generate_html_diff(project_config, pair['left'],
                                               empty_file, diff_dir,
                                               filename, daisydiff_jar)
                change_obj = diff_changes(diff_file)
                if change_obj:
                    deleted_files[filename] = {k: v for k, v in change_obj.items()}
        else: # added
            empty_file = os.path.join(project_config.project_doxygen_doc_dir,
                                      commit_id, 'html', 'add',
                                      'a' + str(len(added_files)),
                                      os.path.basename(pair['right']))
            create_empty_html_file(empty_file)
            copy_daisydiff_files(daisydiff_dir, diff_dir, pair['right'])
            diff_file = generate_html_diff(project_config, empty_file, 
                                           pair['right'], diff_dir,
                                           filename, daisydiff_jar)
            change_obj = diff_changes(diff_file)
            if change_obj:
                added_files[filename] = {k: v for k, v in change_obj.items()}
        index += 1
        bar.update(index)
    bar.finish()

    # write data to excel
    added_files = {k: added_files[k] for k in sorted(added_files)}
    deleted_files = {k: deleted_files[k] for k in sorted(deleted_files)}
    changed_files = {k: changed_files[k] for k in sorted(changed_files)}

    excel_file = os.path.join(os.path.dirname(diff_dir),
                               'diff_changes.xlsx')
    diff_data = {'added_files': added_files,
                 'deleted_files': deleted_files,
                 'changed_files': changed_files}
    write_diff_changes(excel_file, diff_data)
    
    # remove empty html files from 'add' and 'del' directories
    add_dir = os.path.join(project_config.project_doxygen_doc_dir, commit_id,
                           'html', 'add')
    del_dir = os.path.join(project_config.project_doc_dir, commit_id, 'html',
                           'del')
    if os.path.exists(add_dir) and os.path.isdir(add_dir):
        shutil.rmtree(add_dir)
    if os.path.exists(del_dir) and os.path.isdir(del_dir):
        shutil.rmtree(del_dir)


def copy_doxygen_files(doc_dir, diff_dir):
    logging.info('Copying documentation files ...')
    shutil.copytree(doc_dir, diff_dir, dirs_exist_ok=True)


def copy_daisydiff_files(daisydiff_dir, commit_diff_dir, right_file):
    dir_path = os.path.dirname(right_file)
    path_components = dir_path.split('/')
    html_index = len(path_components) - path_components[::-1].index('html') - 1
    dirs_under_html = path_components[html_index + 1 : ]
    dest_dir = os.path.join(commit_diff_dir, '/'.join(dirs_under_html))

    shutil.copytree(os.path.join(daisydiff_dir, 'css'),
                    os.path.join(dest_dir, 'css'), dirs_exist_ok=True)
    shutil.copytree(os.path.join(daisydiff_dir, 'js'),
                    os.path.join(dest_dir, 'js'), dirs_exist_ok=True)
    shutil.copytree(os.path.join(daisydiff_dir, 'images'),
                    os.path.join(dest_dir, 'images'), dirs_exist_ok=True)
    

def extract_filenames(project_config, commit_id):
    """Read html doc file names and extract original file names.
    Store original file names and their corresponding html file names
    in a dictionary.
    """
    # logging.info('Extracting file names ...')

    # original_filename: {
    #     left: commit1 html file path,
    #     right: commit2 html file path
    # }
    data = {} 
    doxygen_doc_dir = os.path.join(project_config.project_doxygen_doc_dir,
                                   commit_id, 'html')
    dir_list = [os.path.join(doxygen_doc_dir, d) for d 
                in os.listdir(doxygen_doc_dir) 
                if os.path.isdir(os.path.join(doxygen_doc_dir, d))]
    for dir in dir_list:
        for root, dirs, files in os.walk(dir):
            for filename_o in files:
                if not filename_o.endswith(('_8c.html', '_8h.html')):
                    continue
                file_path_o = os.path.join(root, filename_o)
                filename = parse_filename(file_path_o)
                if filename is None:
                    logging.error('Failed to extract filename from {}'
                                  .format(file_path_o))
                    continue
                data[filename] = {'left': file_path_o}

    autoconf_doc_dir = os.path.join(project_config.project_doc_dir, commit_id,
                                    'html')
    dir_list = [os.path.join(autoconf_doc_dir, d) for d 
                in os.listdir(autoconf_doc_dir) 
                if os.path.isdir(os.path.join(autoconf_doc_dir, d))]
    for dir in dir_list:
        for root, dirs, files in os.walk(dir):
            for filename_o in files:
                if not filename_o.endswith(('_8c.html', '_8h.html')):
                    continue
                file_path_o = os.path.join(root, filename_o)
                filename = parse_filename(file_path_o)
                if filename is None:
                    logging.error('Failed to extract filename from {}'
                                  .format(file_path_o))
                    continue
                if filename in data:
                    data[filename]['right'] = file_path_o
                else:
                    data[filename] = {'right': file_path_o}
    return data


def parse_filename(html_file_path):
    """Read HTML file and extract original file path from the nav-path div."""
    if not html_file_path or not os.path.isfile(html_file_path):
        return None

    with open( html_file_path, 'rb') as f:
        lines = f.readlines()
    html_content = b''.join(lines).decode(encoding='utf-8', errors='ignore')
    tree = html.fromstring(html_content)

    nav_path_divs = tree.xpath('//div[@id="nav-path"]')
    if len(nav_path_divs) < 1:
        return None
    li_tags = nav_path_divs[0].xpath(f'.//ul/li[@class="navelem"]/a[@class="el"]')
    paths = [a.text.strip() for a in li_tags if a.text.strip() is not None]
    if len(paths) < 1:
        return None
    filename = os.path.join(*paths)
    return filename
                

