def generate_html_diff(project_config, left_file, right_file, commit_diff_dir,
                       original_filename, daisydiff_jar):
    # logging.info('Comparing file {} ...'.format(original_filename))
    path_components = right_file.split('/')
    path_components.reverse()
    html_index = path_components.index('html')
    parent_path = '/'.join(['..'] * (html_index - 1))
    commit_id = path_components[html_index + 1]

    paths_under_html = path_components[ : html_index]
    paths_under_html.reverse()
    output_file = os.path.join(commit_diff_dir, '/'.join(paths_under_html))

    css_files = ['tabs.css', 'navtree.css', 'search/search.css', 'doxygen.css']
    css_files = [os.path.join(parent_path, item) for item in css_files]
    css_files = ';'.join(css_files)

    js_files = ['jquery.js', 'dynsections.js', 'resize.js', 'navtreedata.js',
                'navtree.js', 'search/searchdata.js', 'search/search.js']
    js_files = [os.path.join(parent_path, item) for item in js_files]
    js_files = ';'.join(js_files)
    
    command = ['java', '-jar', f'{daisydiff_jar}', f'{left_file}',
                f'{right_file}', f'--file={output_file}',
                f'--css={css_files}', f'--js={js_files}',
                '--doxygen', '--q']
    
    try:
        r = subprocess.run(command, timeout=60*5)
        if r.returncode != 0:
            logging.error(('[{}][html_diff] Failed to generate HTML diff for '
                           'file {} in commit {}')
                           .format(project_config.project_name,
                                   original_filename, commit_id[:7]))
            return None
    except subprocess.TimeoutExpired:
        logging.error(('[{}][html_diff] Timeout to generate HTML diff for '
                        'file {} in commit {}')
                        .format(project_config.project_name,
                                original_filename, commit_id[:7]))
        return None
    return output_file


def diff_changes(diff_file):
    """Read the HTML diff file generated by DaisyDiff, find element with 
    id='first-diff' and check diff has changes or not.
    Also, check changes are only in Kconfig parts, only in non-Kconfig parts
    or both by parsing 'data-kconfig' attributes.
    """
    if not diff_file or not os.path.isfile(diff_file):
        return None
    
    with open( diff_file, 'rb') as f:
        lines = f.readlines()
    html_content = b''.join(lines).decode(encoding='utf-8', errors='ignore')
    tree = html.fromstring(html_content)
    
    data = {'config': 0, 'defined_at': 0, 'used_at': 0, 'non_config': ''}

    # Check any changes exist in the diff file.
    # Parse div with class='diff-topbar'.
    # If there is any change, this div will contain
    # an a tag with id='first-diff'.
    diff_topbar = tree.xpath('//div[@class="diff-topbar"]')
    if len(diff_topbar) < 1:
        return data
    first_diff = diff_topbar[0].xpath('.//a[@id="first-diff"]')
    if len(first_diff) < 1:
        return data
    href_value = first_diff[0].get('href')
    if href_value == '#':
        return data

    changed_definition_count = 0
    changed_define_count = 0
    changed_use_count = 0
    total_config_diff_span = 0

    # Kconfig header summary: 3
    # Kconfigs: 1
    # Each kdef summary: 3
    # Kconfig Documentation title: 1
    extra_html_diff = 3 + 1
    kdef_items = 0

    # Check changes in Kconfig symbol summary part.
    config_summary_table = tree.xpath('//a[@id="kconfig-members"]/ancestor::table[@class="memberdecls"]')
    if len(config_summary_table) > 0:
        diff_html = config_summary_table[0].xpath('.//span[contains(@class, "diff-html-")]')
        total_config_diff_span += len(diff_html)
        # For each newly declared kdef, there are 3 diff-html- in each kdef description.
        # 2 * (diff_html-1)/3
        kdef_items = len(diff_html) - 1
        extra_html_diff += 2 * kdef_items // 3
        extra_html_diff += kdef_items * 3

    # Check changes in Kconfig symbol definition parts.
    config_divs = tree.xpath('//span[@data-kconfig="definition"]/ancestor::div[@class="memdoc"]')
    for div in config_divs:
        definition = div.xpath('.//span[@data-kconfig="definition"]')
        help = div.xpath('.//pre[@data-kconfig="help"]')
        defined_at = div.xpath('.//span[@data-kconfig="defined-at"]')
        used_at = div.xpath('.//span[@data-kconfig="used-at"]')

        def_diff_html, help_diff_html = [], []
        if len(definition) > 0:
            def_diff_html = definition[0].xpath('.//span[contains(@class, "diff-html-")]')
            total_config_diff_span += len(def_diff_html)
        if len(help) > 0:
            help_diff_html = help[0].xpath('.//span[contains(@class, "diff-html-")]')
            total_config_diff_span += len(help_diff_html)
        if len(def_diff_html) > 0 or len(help_diff_html) > 0:
            changed_definition_count += 1

        if len(defined_at) > 0:
            diff_html = defined_at[0].xpath('.//span[contains(@class, "diff-html-")]')
            if len(diff_html) > 0:
                changed_define_count += 1
                total_config_diff_span += len(diff_html)

        if len(used_at) > 0:
            diff_html = used_at[0].xpath('.//span[contains(@class, "diff-html-")]')
            if len(diff_html) > 0:
                changed_use_count += 1
                total_config_diff_span += len(diff_html)

    data['config'] = changed_definition_count
    data['defined_at'] = changed_define_count
    data['used_at'] = changed_use_count

    total_diff_span = tree.xpath('//span[contains(@class, "diff-html-")]')
    if len(total_diff_span) > (total_config_diff_span + extra_html_diff):
        data['non_config'] = 'yes'

    return data


def write_diff_changes(excel_file, diff_data):
    wb = xlsxwriter.Workbook(excel_file)
    ws = wb.add_worksheet()

    filename                = ('filename', 0)
    config_change           = ('config change', 1)
    non_config_change       = ('non-config change', 2)
    changed_symbol_count    = ('config changed symbol count', 3)

    row = 0
    ws.write(row, filename[1], filename[0])
    ws.write(row, config_change[1], config_change[0])
    ws.write(row, non_config_change[1], non_config_change[0])
    ws.write(row, changed_symbol_count[1], changed_symbol_count[0])
    row += 1

    config_yes_count, non_config_yes_count, total_symbol_count = 0, 0, 0
    
    for file in diff_data['added_files']:
        ws.write(row, filename[1], file)
        symbol_count = diff_data['added_files'][file]['config']
        if symbol_count > 0:
            ws.write(row, config_change[1], 'yes')
            ws.write(row, changed_symbol_count[1], symbol_count)
            config_yes_count += 1
            total_symbol_count += symbol_count
        if diff_data['added_files'][file]['non_config']:
            ws.write(row, non_config_change[1],
                     diff_data['added_files'][file]['non_config'])
            non_config_yes_count += 1
        row += 1
    for file in diff_data['deleted_files']:
        ws.write(row, filename[1], file)
        symbol_count = diff_data['deleted_files'][file]['config']
        if symbol_count > 0:
            ws.write(row, config_change[1], 'yes')
            ws.write(row, changed_symbol_count[1], symbol_count)
            config_yes_count += 1
            total_symbol_count += symbol_count
        if diff_data['deleted_files'][file]['non_config']:
            ws.write(row, non_config_change[1],
                     diff_data['deleted_files'][file]['non_config'])
            non_config_yes_count += 1
        row += 1
    for file in diff_data['changed_files']:
        ws.write(row, filename[1], file)
        symbol_count = diff_data['changed_files'][file]['config']
        if symbol_count > 0:
            ws.write(row, config_change[1], 'yes')
            ws.write(row, changed_symbol_count[1], symbol_count)
            config_yes_count += 1
            total_symbol_count += symbol_count
        if diff_data['changed_files'][file]['non_config']:
            ws.write(row, non_config_change[1],
                     diff_data['changed_files'][file]['non_config'])
            non_config_yes_count += 1
        row += 1

    num_rows = ws.dim_rowmax + 1

    # total file
    total = (len(diff_data['added_files']) + len(diff_data['deleted_files'])
             + len(diff_data['changed_files']))
    ws.write(num_rows, filename[1], total)

    # total config change yes count
    ws.write(num_rows, config_change[1], config_yes_count)

    # total non config change
    ws.write(num_rows, non_config_change[1], non_config_yes_count)

    # total config symbol change count
    ws.write(num_rows, changed_symbol_count[1], total_symbol_count)

    ws.freeze_panes(1, 0)
    wb.close()


def copy_excels_to_data_dir(project_config):
    df = read_csv_to_df(project_config.file_commit_ids_versions)
    for _, row in df.iterrows():
        commit_id = row['sha']
        src_file = os.path.join(project_config.project_doxy_auto_diff_dir,
                                commit_id[:7] + '_' + commit_id[:7],
                                'diff_changes.xlsx')
        dst_dir = os.path.join(project_config.project_doxy_diff_data_html_dir,
                                commit_id[:7] + '_' + commit_id[:7])
        dst_file = os.path.join(dst_dir,'diff_changes.xlsx')
        if os.path.isfile(src_file):
            if not os.path.exists(dst_dir):
                os.makedirs(dst_dir)
            shutil.copy(src_file, dst_file)
        else:
            logging.error('[{}] Could not copy Doxygen diff_changes.xlsx file for {}'
                          .format(project_config.project_name, commit_id))


def combine_excels(project_config):
    logging.info('[{}] Combining Doxygen diff_changes.xlsx files for versions'
                 .format(project_config.project_name))
    version_df = read_csv_to_df(project_config.file_commit_ids_versions)

    data = []
    headings = ['Version',
                'Year',
                'Total source code files',
                # Files that have changes because of Kconfig inclusion
                'Total ConfiGen files with Kconfig info',
                # Files that have changes because of source code.
                # It should be 0 and is 0. So, removed.
                # 'Total ConfiGen changed files for source code',
                'Total Kconfig symbol definitions found in ConfiGen files']
    data.append(headings)
    combined_excel = os.path.join(project_config.project_doxy_diff_data_html_dir,
                                  'diff_changes.xlsx')
    with pd.ExcelWriter(combined_excel, engine='xlsxwriter') as writer:
        for _, row in version_df.iterrows():
            tag = row['tag']
            year = row['year']
            commit_id = row['sha']
            excel_file = os.path.join(project_config.project_doxy_diff_data_html_dir,
                                      commit_id[:7] + '_' + commit_id[:7],
                                      'diff_changes.xlsx')
            if os.path.exists(excel_file) and os.path.isfile(excel_file):
                df = pd.read_excel(excel_file)
                df.to_excel(writer, sheet_name=tag, index=False)
                worksheet = writer.sheets[tag]
                worksheet.freeze_panes(1, 0)
                
                last_row_values = df.iloc[-1].values
                sum_row = [tag,
                           year,
                           last_row_values[0],
                           last_row_values[1],
                           last_row_values[3]]
                data.append(sum_row)
            else:
                logging.error('[{}] Doxygen diff file diff_changes.xlsx does not exist for {}'
                              .format(project_config.project_name, commit_id[:7]))

    summary_excel = os.path.join(project_config.project_doxy_diff_data_html_dir,
                                 'diff_changes_summary.xlsx')
    wb = xlsxwriter.Workbook(summary_excel)
    ws = wb.add_worksheet()
    ws.set_column('A:E', 20)

