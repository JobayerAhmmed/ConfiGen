def xml_diff(project_config, commit_id1, commit_id2):
    """Compare XML file of two commits and show the diff in an HTML table.
    
    AutoConfDoc generates two XML files for a commit - one with Kconfig symbol 
    definitions and other with the usage of Kconfig symbols as #ifdef macro
    in source code. This function compares two definition XML files
    and shows the changes in an HTML table.
    The same goes for the usage XML file.

    The final output is an HTML file containing two tables - one is for symbol
    definitions and another one is for macro usage.

    Note: before using this function, make sure that the XML files of the 
    commits and their parent commits are generated by AutoConfDoc
    and kept in the corresponding project XML directory 
    (see variable project_xml_dir in config_*.py file).
    """
    logging.info('[{}] Generating XML diff between commit {} and {}'.format(
        project_config.project_name, commit_id1[:7], commit_id2[:7]))

    config_table = create_config_table(project_config, commit_id1, commit_id2)
    # usage_table = create_usage_table(project_config, commit_id1, commit_id2)

    if config_table is None:
        logging.error('[{}][xml_diff] Failed to generate XML diff between {} and {}'
                      .format(project_config.project_name, commit_id1[:7], commit_id2[:7]))
        return

    html = get_html(commit_id1, commit_id2, config_table)
    output_file = os.path.join(project_config.project_xml_diff_dir,
                               commit_id1[:7] + '_' + commit_id2[:7]
                               + '_xml_diff.html')
    with open(output_file, 'w') as f:
        f.write(html)


def create_config_table(project_config, commit_id1, commit_id2):
    left_config_xml = os.path.join(project_config.project_xml_dir,
                                   commit_id1 + '_defs.xml')
    right_config_xml = os.path.join(project_config.project_xml_dir,
                                    commit_id2 + '_defs.xml')

    if not os.path.isfile(left_config_xml):
        logging.error('[{}][xml_diff] defs.xml file not found for commit {}'
                      .format(project_config.project_name, commit_id1[:7]))
        return None
    if not os.path.isfile(right_config_xml):
        logging.error('[{}][xml_diff] defs.xml file not found for commit {}'
                      .format(project_config.project_name, commit_id2[:7]))
        return None

    data = get_kdef_data(left_config_xml, right_config_xml)
    df = pd.DataFrame.from_dict(data, orient='index')

    colored_df = color_kdef(df)
    if not colored_df.empty:
        colored_df['parent_kdef'] = colored_df['parent_kdef'].apply(
            lambda x: x['info'] if x else '')
        colored_df['commit_kdef'] = colored_df['commit_kdef'].apply(
            lambda x: x['info'] if x else '')
        colored_df['parent_kdef'] = colored_df['parent_kdef'].apply(
            lambda x: x.replace('\n', '<br>')
            .replace('\t', '&nbsp;&nbsp;&nbsp;&nbsp;') if x else '')
        colored_df['commit_kdef'] = colored_df['commit_kdef'].apply(
            lambda x: x.replace('\n', '<br>')
            .replace('\t', '&nbsp;&nbsp;&nbsp;&nbsp;') if x else '')
        colored_df = colored_df.reset_index()
        colored_df.columns = ['Kconfig', 'Commit ' + commit_id1[:7],
                              'Commit ' + commit_id2[:7]]
    table = colored_df.to_html(None, escape=False, bold_rows=False, 
                               index=False)
    return table


def get_kdef_data(left_kdef_xml, right_kdef_xml):
    # {filename: {kdef_name: kdef_info}}
    left_kdefs = parse_kdef_xml(left_kdef_xml)
    right_kdefs = parse_kdef_xml(right_kdef_xml)

    common_filenames = list(set(left_kdefs.keys()) & set(right_kdefs.keys()))
    left_filenames = list(set(left_kdefs.keys()) - set(right_kdefs.keys()))
    right_filenames = list(set(right_kdefs.keys()) - set(left_kdefs.keys()))
    common_filenames.sort()
    left_filenames.sort()
    right_filenames.sort()

    # {kdef_name: (kdef, kdef)}
    result = {}
    # {kdef_name: kdef}
    all_left_kdefs = {}
    all_right_kdefs = {}
    for filename in common_filenames:
        common_kdef_names = list(set(left_kdefs[filename].keys()) 
                                 & set(right_kdefs[filename].keys()))
        left_kdef_names = list(set(left_kdefs[filename].keys()) 
                               - set(right_kdefs[filename].keys()))
        right_kdef_names = list(set(right_kdefs[filename].keys()) 
                                - set(left_kdefs[filename].keys()))
        common_kdef_names.sort()
        left_kdef_names.sort()
        right_kdef_names.sort()

        for item in common_kdef_names:
            left_kdef = left_kdefs[filename][item]
            right_kdef = right_kdefs[filename][item]
            if item in all_left_kdefs:
                logging.error('Duplicate Kconfig in parent: {}'.format(item))
            else:
                all_left_kdefs[item] = left_kdef
            if item in all_right_kdefs:
                logging.error('Duplicate Kconfig in commit: {}'.format(item))
            else:
                all_right_kdefs[item] = right_kdef

        for item in left_kdef_names:
            left_kdef = left_kdefs[filename][item]
            if item in all_left_kdefs:
                logging.error('Duplicate Kconfig in parent: {}'.format(item))
            else:
                all_left_kdefs[item] = left_kdef

        for item in right_kdef_names:
            right_kdef = right_kdefs[filename][item]
            if item in all_right_kdefs:
                logging.error('Duplicate Kconfig in commit: {}'.format(item))
            else:
                all_right_kdefs[item] = right_kdef

    for filename in left_filenames:
        for item in left_kdefs[filename].keys():
            left_kdef = left_kdefs[filename][item]
            if item in all_left_kdefs:
                logging.error('Duplicate Kconfig in parent: {}'.format(item))
            else:
                all_left_kdefs[item] = left_kdef

    for filename in right_filenames:
        for item in right_kdefs[filename].keys():
            right_kdef = right_kdefs[filename][item]
            if item in all_right_kdefs:
                logging.error('Duplicate Kconfig in commit: {}'.format(item))
            else:
                all_right_kdefs[item] = right_kdef

    common_kdef_names = list(set(all_left_kdefs.keys()) 
                                 & set(all_right_kdefs.keys()))
    left_kdef_names = list(set(all_left_kdefs.keys()) 
                            - set(all_right_kdefs.keys()))
    right_kdef_names = list(set(all_right_kdefs.keys()) 
                            - set(all_left_kdefs.keys()))
    
    common_kdef_names.sort()
    left_kdef_names.sort()
    right_kdef_names.sort()

    for item in common_kdef_names:
        left_kdef = all_left_kdefs[item]
        right_kdef = all_right_kdefs[item]
        if left_kdef['info'] != right_kdef['info']:
            if item in result:
                logging.error('Kconfig already exist in result {}'.format(item))
            else:
                result[item] = (left_kdef, right_kdef)

    for item in left_kdef_names:
        left_kdef = all_left_kdefs[item]
        if item in result:
            logging.error('Left Kconfig already exist in result {}'.format(item))
        else:
            result[item] = (left_kdef, None)

    for item in right_kdef_names:
        right_kdef = all_right_kdefs[item]
        if item in result:
            logging.error('Right Kconfig already exist in result {}'.format(item))
        else:
            result[item] = (None, right_kdef)

    # reshape the data to a flat dictionary
    flat_data = {}
    for k, v in result.items():
        flat_data[k] = {'parent_kdef': v[0], 
                            'commit_kdef': v[1]}

    sorted_data = {k: flat_data[k] for k in sorted(flat_data)}
    return sorted_data


