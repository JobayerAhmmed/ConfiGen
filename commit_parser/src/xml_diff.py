def get_colored_parts(left_str, right_str, attr_name):
    left_parts, right_parts = [], []

    attr_str = '\n\t' + attr_name
    if attr_name:
        attr_str += ' '

    if left_str == right_str:
        if len(left_str) == 0:
            return ([],[])
        left_parts.append(attr_str + left_str)
        right_parts.append(attr_str + right_str)
        return (left_parts, right_parts)
        
    if left_str:
        left_parts.append(attr_str)
    if right_str:
        right_parts.append(attr_str)

    s = SequenceMatcher(lambda x: x in " \t", left_str, right_str)
    for tag, i1, i2, j1, j2 in s.get_opcodes():
        if tag == 'equal':
            left_parts.append(left_str[i1:i2])
            right_parts.append(right_str[j1:j2])
        elif tag == 'insert':
            s = add_class(tag, right_str[j1:j2])
            right_parts.append(s)
        elif tag == 'delete':
            s = add_class(tag, left_str[i1:i2])
            left_parts.append(s)
        elif tag == 'replace':
            s = add_class('delete', left_str[i1:i2])
            left_parts.append(s)
            s = add_class('insert', right_str[j1:j2])
            right_parts.append(s)
    return (left_parts, right_parts)


def add_class(class_name: str, text: str):
    s = '<span class="{}">{}</span>'.format(class_name, text)
    return s


