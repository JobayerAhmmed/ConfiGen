def parse_kdef_xml(xml_file: str):
    parser = etree.XMLParser(encoding='ascii', recover=True)
    tree = etree.parse(xml_file, parser=parser)
    node_KconfigRoot = tree.getroot()
    # {filename: {kdef_name: kdef_info}}
    # kdef_info = {
    #   name: str,
    #   type: str,
    #   prompt: str,
    #   defaults: list(str),
    #   depends: str,
    #   selects: list(str),
    #   implies: list(str),
    #   ranges: list(str),
    #   help: str
    #   lineno: int,
    #   info: str
    # }
    data = {}
    for node_file in node_KconfigRoot:
        filename = node_file.attrib['fileName']
        for node_Kconfig in node_file:
            kdef = {
                'name': '',
                'type': '',
                'prompt': '',
                'defaults': [],
                'depends': '',
                'selects': [],
                'implies': [],
                'ranges': [],
                'help': '',
                'lineno': -1,
                'info': ''
            }
            info = 'config '
            name = node_Kconfig.attrib['name']
            kdef['name'] = name
            info += name
            type = node_Kconfig.find('type')
            kdef['type'] = type.text
            info += '\n\t' + type.text
            prompt = node_Kconfig.find('prompt')
            if prompt is not None:
                text = prompt.text
                item_depends = prompt.attrib['depends']
                if item_depends:
                    text += ' if ' + item_depends
                kdef['prompt'] = text
                info += ' ' + text
            defaults = node_Kconfig.findall('default')
            if defaults is not None:
                for item in defaults:
                    text = item.text
                    item_depends = item.attrib['depends']
                    if item_depends:
                        text += ' if ' + item_depends
                    kdef['defaults'].append(text)
                    info += '\n\tdefault ' + text
            depends = node_Kconfig.find('depends')
            if depends is not None:
                kdef['depends'] = depends.text
                info += '\n\tdepends on ' + depends.text
            selects = node_Kconfig.findall('select')
            if selects is not None:
                for item in selects:
                    text = item.text
                    item_depends = item.attrib['depends']
                    if item_depends:
                        text += ' if ' + item_depends
                    kdef['selects'].append(text)
                    info += '\n\tselect ' + text
            implies = node_Kconfig.findall('imply')
            if implies is not None:
                for item in implies:
                    text = item.text
                    item_depends = item.attrib['depends']
                    if item_depends:
                        text += ' if ' + item_depends
                    kdef['implies'].append(text)
                    info += '\n\timply ' + text
            ranges = node_Kconfig.findall('range')
            if ranges is not None:
                for item in ranges:
                    text = item.text
                    item_depends = item.attrib['depends']
                    if item_depends:
                        text += ' if ' + item_depends
                    kdef['ranges'].append(text)
                    info += '\n\trange ' + text
            help = node_Kconfig.find('help')
            if help is not None:
                help_text = '\n\t\t' + help.text.replace('\n', '\n\t\t')
                kdef['help'] = help_text
                info += '\n\thelp' + help_text
            lineno = node_Kconfig.find('lineno')
            if lineno is not None:
                kdef['lineno'] = lineno
            kdef['info'] = info
            if filename in data:
                data[filename][name] = copy.deepcopy(kdef)
            else:
                data[filename] = {name: copy.deepcopy(kdef)}
    return data



def check_kdef_del_usage_exist(project_config):
    """This function is to check whether any macro usage exist in source code 
    for a deleted Kconfig symbol definition.
    """
    kdef_df = read_csv_to_df(project_config.file_kdef)
    kdef_df['commit.committer.date'] = pd.to_datetime(
        kdef_df['commit.committer.date'])
    kdef_df = kdef_df.sort_values('commit.committer.date', ascending=True)
    kdef_df = kdef_df.reset_index(drop=True)
    kdef_df['files'] = kdef_df['files'].apply(ast.literal_eval)
    kdef_df['defs_move'] = kdef_df['defs_move'].apply(ast.literal_eval)

    doc_gen_df = read_csv_to_df(project_config.file_commit_ids_doc_gen_status)
    doc_gen_df['commit.committer.date'] = pd.to_datetime(
        doc_gen_df['commit.committer.date'])
    doc_gen_df = doc_gen_df.sort_values(
        'commit.committer.date', ascending=True)
    doc_gen_df = doc_gen_df.reset_index(drop=True)

    commit_ids_del = []
    for _, row in kdef_df.iterrows():
        doc_gen_rows = doc_gen_df.loc[doc_gen_df['sha'] == row['sha']]
        if doc_gen_rows.empty:
            continue
        if doc_gen_rows.iloc[0]['doc_gen_status'] == 'fail':
            continue
        usage_csv = os.path.join(
            project_config.project_xml_dir, row['sha'] + '_usage.csv')
        usage_df = read_csv_to_df(usage_csv)
        usage_df['files'] = usage_df['files'].apply(ast.literal_eval)
        for file in row['files']:
            only_del = file['only_del']
            is_exist = False
            for item in only_del:
                is_exist = (usage_df['kdef'] == item).any()
                if is_exist:
                    commit_ids_del.append(row['sha'])
                    break
            if is_exist:
                break
    logging.info('commit_ids_for_kdef_del_usage_exist for {}\n{}'
                 .format(project_config.project_name,
                         '\n'.join(commit_ids_del)))


